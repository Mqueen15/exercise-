def start_K(l):
    if l[0] == "K":
        return True
    else:
        return False

# A function call like this should return True:
print(start_K("Kelly"))

# And one like this should return False:
print(start_K("Abe"))

# And one like this should return False:
print(start_K("Maram"))

# And one like this should return True:
print(start_K("Katy")) 
